#include "libft.h"
#include <unistd.h>

void	ft_putendl_fd(char *s, int fd)
{
	if (!s)
		write(fd, "(null)", sizeof("(null)") - 1);
	else
		write(fd, s, str_len(s));
	write(fd, "\n", 1);
}
